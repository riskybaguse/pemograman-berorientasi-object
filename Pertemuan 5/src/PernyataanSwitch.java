public class PernyataanSwitch {
    public static void main(String[] args) {
        int pilihan = 4;
        switch (pilihan){
            case 1:
                System.out.println("Pilihan 1");
                break;
            case 2:
                System.out.println("Pilihan 2");
                break;
            case 3:
                System.out.println("Pilihan 3");
            default:
                System.out.println("Pilihan tidak ada");
        }
    }
}
